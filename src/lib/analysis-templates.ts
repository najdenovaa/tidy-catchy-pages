// ─── Extract data from text ──────────────────────────────────────

export interface ExtractedValue {
  category: string;
  label: string;
  value: string;
  raw: string;
}

export function extractValuesFromText(text: string): ExtractedValue[] {
  const results: ExtractedValue[] = [];
  const seen = new Set<string>();
  
  for (const pattern of KEYWORD_PATTERNS) {
    const regex = new RegExp(pattern.regex.source, pattern.regex.flags);
    let match: RegExpExecArray | null;
    while ((match = regex.exec(text)) !== null) {
      const key = `${pattern.category}:${match[0].trim().toLowerCase()}`;
      if (!seen.has(key)) {
        seen.add(key);
        results.push({
          category: pattern.category,
          label: pattern.label,
          value: match[1] || match[0],
          raw: match[0].trim(),
        });
      }
      if (results.length > 200) break; // Safety limit
    }
  }
  
  return results;
}

// ─── Template selector ───────────────────────────────────────────

export function getTemplate(
  templateSet: string[],
  seed?: number
): string {
  return pick(templateSet, seed);
}

export function getMultipleTemplates(
  templateSet: string[],
  count: number,
  startSeed?: number
): string[] {
  const selected: string[] = [];
  const used = new Set<number>();
  for (let i = 0; i < Math.min(count, templateSet.length); i++) {
    let idx = ((startSeed || 0) + i * 7) % templateSet.length;
    while (used.has(idx) && used.size < templateSet.length) {
      idx = (idx + 1) % templateSet.length;
    }
    used.add(idx);
    selected.push(templateSet[idx]);
  }
  return selected;
}
